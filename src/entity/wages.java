package entity;

public class wages {
	private String jobnum;
	private String basicmoney;
	private String levelmoney;
	private String oldmoney;
	private String unemploymentmoney;
	private String Accumulationmoney;
	private String paymoney;
	public String getJobnum() {
		return jobnum;
	}
	public void setJobnum(String jobnum) {
		this.jobnum = jobnum;
	}
	public String getBasicmoney() {
		return basicmoney;
	}
	public void setBasicmoney(String basicmoney) {
		this.basicmoney = basicmoney;
	}
	public String getLevelmoney() {
		return levelmoney;
	}
	public void setLevelmoney(String levelmoney) {
		this.levelmoney = levelmoney;
	}
	public String getOldmoney() {
		return oldmoney;
	}
	public void setOldmoney(String oldmoney) {
		this.oldmoney = oldmoney;
	}
	public String getUnemploymentmoney() {
		return unemploymentmoney;
	}
	public void setUnemploymentmoney(String unemploymentmoney) {
		this.unemploymentmoney = unemploymentmoney;
	}
	public String getAccumulationmoney() {
		return Accumulationmoney;
	}
	public void setAccumulationmoney(String accumulationmoney) {
		Accumulationmoney = accumulationmoney;
	}
	public String getPaymoney() {
		return paymoney;
	}
	public void setPaymoney(String paymoney) {
		this.paymoney = paymoney;
	}
}
